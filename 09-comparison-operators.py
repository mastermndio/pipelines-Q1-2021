# Is equals?
print("6" == "6")

# Greater Than
print(7 > 6)

#Less than
print(6 < 7)

# Greater Than or equal to
print(7 >= 7)

#Less than or equal to
print(6 <= 7)

# And operation(Both need to be true for this to evaluate to true)
print("6" == "6" and 6 > 8)

# Or operator(Only one needs to evaluate to true for this to be true1)
print(7 > 6 or 8 == 9)